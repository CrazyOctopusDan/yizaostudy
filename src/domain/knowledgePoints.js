function pointWeight(chapterIndex, sectionIndex, itemIndex) {
  if (chapterIndex <= 1 || sectionIndex === 0 || itemIndex === 0) return 'high';
  if (chapterIndex <= 3) return 'medium';
  return 'normal';
}

function pointMinutes(importance) {
  if (importance === 'high') return 35;
  if (importance === 'medium') return 30;
  return 25;
}

export const KNOWLEDGE_POINTS = TEXTBOOK_TREE.flatMap((subject) =>
  subject.chapters.flatMap((chapterItem, chapterIndex) =>
    chapterItem.sections.flatMap((sectionItem, sectionIndex) =>
      sectionItem.items.map((title, itemIndex) => {
        const importance = pointWeight(chapterIndex, sectionIndex, itemIndex);
        return {
          id: `${subject.subjectId}-${String(chapterIndex + 1).padStart(2, '0')}-${String(sectionIndex + 1).padStart(2, '0')}-${String(itemIndex + 1).padStart(2, '0')}`,
          subjectId: subject.subjectId,
          chapter: chapterItem.title,
          section: sectionItem.title,
          title,
          estimatedMinutes: pointMinutes(importance),
          importance,
          sourceUrl: subject.sourceUrl,
        };
      }),
    ),
  ),
);

export function getKnowledgePointsForSubject(subjectId) {
  return KNOWLEDGE_POINTS.filter((point) => point.subjectId === subjectId);
}

export function getKnowledgePoint(pointId) {
  return KNOWLEDGE_POINTS.find((point) => point.id === pointId);
}

export function getTextbookTreeForSubject(subjectId) {
  return TEXTBOOK_TREE.find((subject) => subject.subjectId === subjectId);
}
